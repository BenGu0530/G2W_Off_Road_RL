# FIG 10 — Summary Numbers Banner
# ═══════════════════════════════════════════════════════════════════════════

def fig10_summary():
    """Big numbers at-a-glance summary for poster results header."""
    s = pd.read_csv("comparison_data_walk_shallow.csv")

    # Stats
    goals_reached = s["goal_idx"].max() + 1
    total_goals = 5
    total_dist = s["distance_cumulative"].max()
    total_time_s = len(s) * 0.02
    mean_speed = s["speed"].mean()
    final_cot = s["cot_cumulative"].iloc[-1]

    fig, ax = plt.subplots(figsize=(14, 3.5))
    ax.axis("off")

    # 4 big numbers
    numbers = [
        (f"{goals_reached}/{total_goals}", "Goals reached", "#4CAF50"),
        (f"{total_dist:.0f} m",              "Distance traveled", COLOR_ACCENT),
        (f"{mean_speed:.2f} m/s",            "Mean speed", "#FF9800"),
        (f"{final_cot:.2f}",                  "Cost of Transport", "#9C27B0"),
    ]

    for i, (big, small, color) in enumerate(numbers):
        x_center = (i + 0.5) / len(numbers)
        ax.text(x_center, 0.6, big, ha="center", va="center",
                fontsize=48, fontweight="bold", color=color,
                transform=ax.transAxes)
        ax.text(x_center, 0.15, small, ha="center", va="center",
                fontsize=POSTER_FONT_SIZE + 2, color="#333",
                transform=ax.transAxes)

    # Dividers between numbers
    for i in range(1, len(numbers)):
        x = i / len(numbers)
        ax.plot([x, x], [0.1, 0.9], color="#CCC", linewidth=1,
                transform=ax.transAxes)

    ax.set_title("Experiment summary (shallow terrain, 5-goal inspection task)",
                 fontsize=POSTER_FONT_SIZE + 1, pad=15)

    plt.tight_layout()
    plt.savefig("fig10_summary_numbers.png")
    plt.close()
    print("[OK] fig10_summary_numbers.png")


