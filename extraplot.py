# FIG 9 — Field Schematic with Dimensions
# ═══════════════════════════════════════════════════════════════════════════

def fig9_field_schematic():
    """Clean labeled diagram of the agricultural field."""
    fig, ax = plt.subplots(figsize=(10, 11))

    x_min, x_max = -TOTAL_X / 2, TOTAL_X / 2
    ix_min, ix_max = -INNER_X / 2, INNER_X / 2

    color_map = {
        "offroad":  COLOR_OFFROAD,
        "pavement": COLOR_PAVEMENT,
        "crop":     COLOR_CROP,
    }
    alpha_map = {"offroad": 0.35, "pavement": 0.45, "crop": 0.65}

    def add_rect(x0, x1, y0, y1, kind):
        rect = patches.Rectangle((x0, y0), x1 - x0, y1 - y0,
                                 facecolor=color_map[kind], alpha=alpha_map[kind],
                                 edgecolor="none", zorder=1)
        ax.add_patch(rect)

    y = -TOTAL_Y / 2.0
    zones = []
    zones.append(("offroad", y, y + BORDER_Y)); y += BORDER_Y
    for i in range(5):
        zones.append(("offroad", y, y + OFFROAD_Y)); y += OFFROAD_Y
        zones.append(("crop",    y, y + CROP_Y));    y += CROP_Y
        zones.append(("offroad", y, y + OFFROAD_Y)); y += OFFROAD_Y
        if i < 4:
            zones.append(("pavement", y, y + PAVEMENT_Y)); y += PAVEMENT_Y
    zones.append(("offroad", y, y + BORDER_Y))

    for ztype, ys, ye in zones:
        if ztype == "crop":
            add_rect(x_min, ix_min, ys, ye, "offroad")
            add_rect(ix_min, ix_max, ys, ye, "crop")
            add_rect(ix_max, x_max, ys, ye, "offroad")
        elif ztype == "pavement":
            # Pavement is also only in inner_x; borders (|x| > 10) are offroad
            add_rect(x_min, ix_min, ys, ye, "offroad")
            add_rect(ix_min, ix_max, ys, ye, "pavement")
            add_rect(ix_max, x_max, ys, ye, "offroad")
        else:
            add_rect(x_min, x_max, ys, ye, ztype)

    # Goal positions
    goals = [(5.7, -16), (6.5, -8), (-1.5, 0), (7.0, 8), (-1.3, 16)]
    for i, (gx, gy) in enumerate(goals):
        ax.plot(gx, gy, marker="*", markersize=28, color="#FFD600",
                markeredgecolor="black", markeredgewidth=1.5, zorder=5)
        ax.annotate(f"G{i+1}", (gx, gy), xytext=(10, 0),
                    textcoords="offset points", fontsize=POSTER_FONT_SIZE,
                    fontweight="bold", zorder=5)

    # Overall field dimensions
    ax.annotate("", xy=(x_min, -TOTAL_Y / 2 - 2.0), xytext=(x_max, -TOTAL_Y / 2 - 2.0),
                arrowprops=dict(arrowstyle="<->", color="black", lw=1.5))
    ax.text(0, -TOTAL_Y / 2 - 3.2, "24 m", ha="center",
            fontsize=POSTER_FONT_SIZE - 1, fontweight="bold")

    ax.annotate("", xy=(x_max + 1.5, -TOTAL_Y / 2), xytext=(x_max + 1.5, TOTAL_Y / 2),
                arrowprops=dict(arrowstyle="<->", color="black", lw=1.5))
    ax.text(x_max + 2.5, 0, "42 m", rotation=90, ha="center", va="center",
            fontsize=POSTER_FONT_SIZE - 1, fontweight="bold")

    # Strip callouts — moved farther right to avoid 42m label
    ax.annotate("Pavement (2 m)", xy=(x_max + 0.3, -4), xytext=(x_max + 6, -4),
                fontsize=POSTER_FONT_SIZE - 2, fontweight="bold", color=COLOR_PAVEMENT,
                arrowprops=dict(arrowstyle="->", color=COLOR_PAVEMENT, lw=1.5),
                va="center")

    ax.annotate("Crop row (2 m, 25cm barrier)", xy=(ix_max, 0),
                xytext=(x_max + 6, 3),
                fontsize=POSTER_FONT_SIZE - 2, fontweight="bold", color=COLOR_CROP,
                arrowprops=dict(arrowstyle="->", color=COLOR_CROP, lw=1.5),
                va="center")

    ax.annotate("Off-road (rough)", xy=(x_max + 0.3, -10), xytext=(x_max + 6, -10),
                fontsize=POSTER_FONT_SIZE - 2, fontweight="bold", color=COLOR_OFFROAD,
                arrowprops=dict(arrowstyle="->", color=COLOR_OFFROAD, lw=1.5),
                va="center")

    # Legend outside plot, bottom
    legend_patches = [
        patches.Patch(facecolor=COLOR_PAVEMENT, alpha=0.45, label="Pavement (flat)"),
        patches.Patch(facecolor=COLOR_OFFROAD, alpha=0.35, label="Off-road (rough)"),
        patches.Patch(facecolor=COLOR_CROP, alpha=0.65, label="Crop row (barrier)"),
    ]
    ax.legend(handles=legend_patches, loc="upper center",
              bbox_to_anchor=(0.5, -0.08), ncol=3, frameon=False,
              fontsize=POSTER_FONT_SIZE - 2)

    # Give plot more room — wider x range for callouts
    ax.set_xlim(x_min - 2, x_max + 16)
    ax.set_ylim(-TOTAL_Y / 2 - 5, TOTAL_Y / 2 + 2)
    ax.set_aspect("equal")
    ax.set_xlabel("X (m)")
    ax.set_ylabel("Y (m)")
    ax.set_title("Agricultural Field Layout\n5 crop rows with alternating off-road and pavement strips",
                 fontsize=POSTER_FONT_SIZE + 2, pad=15)

    plt.tight_layout()
    plt.savefig("fig9_field_schematic.png")
    plt.close()
    print("[OK] fig9_field_schematic.png")


