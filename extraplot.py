# FIG 8 — System Pipeline Diagram
# ═══════════════════════════════════════════════════════════════════════════

def fig8_pipeline():
    """Block diagram showing how goal -> nav -> policy -> robot."""
    fig, ax = plt.subplots(figsize=(9, 8))
    ax.set_xlim(0, 10)
    ax.set_ylim(0, 11)
    ax.axis("off")

    # Block style
    def add_block(x, y, w, h, text, color, fontsize=POSTER_FONT_SIZE,
                  subtext=None, subcolor="#666"):
        box = FancyBboxPatch((x - w/2, y - h/2), w, h,
                             boxstyle="round,pad=0.1",
                             linewidth=2, edgecolor="black",
                             facecolor=color, alpha=0.85)
        ax.add_patch(box)
        ax.text(x, y + (0.15 if subtext else 0), text,
                ha="center", va="center", fontsize=fontsize, fontweight="bold")
        if subtext:
            ax.text(x, y - 0.25, subtext, ha="center", va="center",
                    fontsize=fontsize - 3, color=subcolor, style="italic")

    def add_arrow(x0, y0, x1, y1, label=None):
        arrow = FancyArrowPatch((x0, y0), (x1, y1),
                                arrowstyle="->,head_width=0.4,head_length=0.7",
                                linewidth=2.5, color="black",
                                mutation_scale=15)
        ax.add_patch(arrow)
        if label:
            mx, my = (x0 + x1) / 2, (y0 + y1) / 2
            ax.text(mx + 0.3, my, label, fontsize=POSTER_FONT_SIZE - 3,
                    style="italic", color=COLOR_ACCENT, fontweight="bold",
                    va="center")

    # Goal list (top)
    add_block(5, 10, 4, 0.9, "Goal list (5 crop row centers)",
              "#FFECB3", subtext="World coordinates")

    # Navigation controller
    add_block(5, 7.8, 5, 1.4, "Navigation Controller",
              "#B3E5FC",
              subtext="Path planning + waypoint tracking + safety checks")

    # Policy
    add_block(5, 5.0, 5, 1.4, "Stage B Policy (PPO)",
              "#C8E6C9",
              subtext="57-dim obs → 16-dim action (12 legs + 4 wheels)")

    # Robot
    add_block(5, 2.2, 5, 1.4, "Go2-W Robot (MuJoCo / Isaac Sim)",
              "#FFCCBC",
              subtext="Wheeled-legged quadruped on agricultural terrain")

    # Arrows between blocks
    add_arrow(5, 9.5, 5, 8.55)
    add_arrow(5, 7.05, 5, 5.75, label="$(v_x, v_y, \\omega_z)$")
    add_arrow(5, 4.25, 5, 2.95, label="joint targets")

    # Feedback arrow from robot back to nav (pose)
    feedback = FancyArrowPatch((7.55, 2.2), (9.3, 2.2),
                               arrowstyle="-", linewidth=2, color="gray", linestyle="--")
    ax.add_patch(feedback)
    feedback2 = FancyArrowPatch((9.3, 2.2), (9.3, 7.8),
                                arrowstyle="-", linewidth=2, color="gray", linestyle="--")
    ax.add_patch(feedback2)
    feedback3 = FancyArrowPatch((9.3, 7.8), (7.55, 7.8),
                                arrowstyle="->,head_width=0.4,head_length=0.7",
                                linewidth=2, color="gray", linestyle="--",
                                mutation_scale=15)
    ax.add_patch(feedback3)
    ax.text(9.5, 5.0, "robot pose\n(feedback)", fontsize=POSTER_FONT_SIZE - 3,
            style="italic", color="gray", ha="left", va="center")

    ax.set_title("System Architecture: Classical nav + RL locomotion",
                 fontsize=POSTER_FONT_SIZE + 3, fontweight="bold", pad=15)

    plt.tight_layout()
    plt.savefig("fig8_pipeline_diagram.png")
    plt.close()
    print("[OK] fig8_pipeline_diagram.png")


